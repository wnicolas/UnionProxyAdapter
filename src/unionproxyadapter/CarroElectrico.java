package unionproxyadapter;

public class CarroElectrico {
	
	public void cargar() {
		System.out.println("Conectando carro eléctrico a fuente de poder.");
	}
	
	public void activar() {
		System.out.println("Activando carro eléctrico.");
	}
	
	public void iniciarSistemaOperativo() {
		System.out.println("Iniciando sistema operativo de carro eléctrico.");
	}
	
	public void acelerar() {
		System.out.println("Acelerando carro eléctrico");
	}
	
	public void frenar() {
		System.out.println("Frenando carro eléctrico.");
	}
	
	public void detenerSistemaOperativo() {
		System.out.println("Deteniendo sistema operativo de carro eléctrico.");
	}
	
	public void desactivar() {
		System.out.println("Desacivando carro eléctrico.");
	}

}
